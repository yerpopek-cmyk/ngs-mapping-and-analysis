#!/bin/bash

# ==============================================================================
# Скрипт 01: Структурная и функциональная аннотация генома
# ------------------------------------------------------------------------------
# Данный скрипт автоматизирует поиск генов (Prodigal) и функциональную 
# аннотацию прокариот с помощью конвейера Prokka. Также показан пример 
# инициализации Nextflow для тяжелых функциональных вычислений.
# ==============================================================================

# Останавливаем скрипт при любой ошибке
set -e

# === Вводные параметры ===
ASSEMBLY_FASTA="../data/my_assembly.fasta"
OUT_DIR="../results/annotation"
PREFIX="my_genome"

echo "🧬 Запуск Prokka для структурной аннотации..."
# Команда prokka ищет ORF, tRNA, rRNA. 
# Использует Prodigal (динамическое программирование, log-likelihood скоринг) под капотом.
prokka \
    --outdir ${OUT_DIR} \
    --prefix ${PREFIX} \
    --kingdom Bacteria \
    --cpus 8 \
    --force \
    --addgenes \
    ${ASSEMBLY_FASTA}

echo "✅ Prokka завершила работу. Ключевые файлы в ${OUT_DIR}:"
echo "  - ${PREFIX}.gff: координаты фичей (GFF3 формат)"
echo "  - ${PREFIX}.faa: аминокислотные последовательности (FASTA, для KEGG/Blast)"

# === Дополнительная статистика (QC) ===
echo "📊 Статистика по аннотации:"
echo "Общее количество предсказанных белков (CDS):"
grep -c ">" ${OUT_DIR}/${PREFIX}.faa

echo "Количество гипотетических (неизвестных) белков:"
grep -c "hypothetical protein" ${OUT_DIR}/${PREFIX}.faa

