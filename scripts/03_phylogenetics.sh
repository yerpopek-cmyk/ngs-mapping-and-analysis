#!/bin/bash

# ==============================================================================
# Скрипт 03: Множественное выравнивание и Филогенетика
# ------------------------------------------------------------------------------
# Использование эвристики и Быстрого Преобразования Фурье (FFT) 
# для выравнивания последовательностей. Подготовка к построению Neighbor-Joining 
# дерева (коррекция "притяжения длинных ветвей" через Q-матрицу).
# ==============================================================================

set -e

FASTA_IN="../data/RPS.fasta"
FASTA_OUT="../results/RPS_aligned.fasta"

# Проверка наличия MAFFT
if ! command -v mafft &> /dev/null
then
    echo "❌ Ошибка: mafft не установлен. Используйте 'conda install mafft'"
    exit
fi

echo "🌳 Запуск множественного выравнивания (MSA) с MAFFT"
# mafft --auto автоматически выбирает стратегию. 
# Для белков и точного выравнивания рекомендуется:
# mafft --localpair --maxiterate 1000 ${FASTA_IN} > ${FASTA_OUT}

mafft --auto ${FASTA_IN} > ${FASTA_OUT}

echo "✅ Выравнивание завершено. Результат сохранен в ${FASTA_OUT}"
echo "------------------------------------------------------------------"
echo "💻 Дальнейшие шаги в MEGA (GUI-инструмент):"
echo "1. Откройте ${FASTA_OUT} в MEGA (File -> Open A File)."
echo "2. Нажмите PHYLOGENY -> Construct/Test Neighbor-Joining Tree."
echo "3. Включите 'Test of Phylogeny -> Bootstrap method' (от 500 до 1000 реплик)."
echo "4. Выберите модель замен (p-distance для ДНК, JTT/LG для белков)."
echo "5. Нажмите Compute для генерации дерева с бутстрап поддержкой."
echo "------------------------------------------------------------------"
